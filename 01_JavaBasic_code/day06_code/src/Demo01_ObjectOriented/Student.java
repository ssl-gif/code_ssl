package Demo01_ObjectOriented;

// 1.类的定义：定义一个类，用来描述 "学生" 这一事物
public class Student {

    // 成员变量
    String name;
    int age;

    // 成员方法
    public void eat() {
        System.out.println("吃饭");
    }

    public void sleep() {
        System.out.println("睡觉");
    }

    public void study() {
        System.out.println("学习");
    }

}
